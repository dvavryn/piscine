#include <unistd.h>

void	ft_putstr(char *str)
{
	int	i;
	int	j;

	i = 0;
	j = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	while (j < i)
	{
		write(1, &str[j], 1);
		j++;
	}
}
